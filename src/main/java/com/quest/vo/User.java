package com.quest.vo;

public class User {
	private String id;
	private String password;
	private String number;
	private String nicname;
	private String email;
	private int phoneNumber;
	private String name;
	private String birth;
	private int ban_confirm;
	private String ban_time;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getNicname() {
		return nicname;
	}
	public void setNicname(String nicname) {
		this.nicname = nicname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(int phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public int getBan_confirm() {
		return ban_confirm;
	}
	public void setBan_confirm(int ban_confirm) {
		this.ban_confirm = ban_confirm;
	}
	public String getBan_time() {
		return ban_time;
	}
	public void setBan_time(String ban_time) {
		this.ban_time = ban_time;
	}
	
	
}
